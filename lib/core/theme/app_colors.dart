import 'package:flutter/material.dart';

class AppColors {
  // ---------------------------------------------------------------------------
  // ФОН И ПОВЕРХНОСТИ
  // ---------------------------------------------------------------------------

  /// Основной фон экрана.
  /// Используется темно-серый вместо #000000 для мягкости.
  static const Color background = Color(0xFF121212);

  /// Цвет для карточек, нижних шторок (BottomSheets), App Bar.
  /// Немного светлее фона для создания объема.
  static const Color surface = Color(0xFF1E1E1E);

  /// Цвет для элементов поверх карточек (например, поля ввода).
  static const Color surfaceLight = Color(0xFF2C2C2C);

  // ---------------------------------------------------------------------------
  // АКЦЕНТЫ (КРАСНЫЙ)
  // ---------------------------------------------------------------------------

  /// Основной акцентный цвет.
  /// Это оттенок (близкий к iOS System Red Dark), который отлично читается на темном.
  static const Color primaryRed = Color(0xFFFF453A);

  /// Приглушенный красный.
  /// Подойдет для фона под активными элементами или состояний нажатия.
  static const Color redMuted = Color(0xFF3E1818);

  // ---------------------------------------------------------------------------
  // ТЕКСТ И ИКОНКИ
  // ---------------------------------------------------------------------------

  /// Основной текст (Заголовки).
  /// Чистый белый для максимального контраста.
  static const Color textPrimary = Color(0xFFFFFFFF);

  /// Вторичный текст (Подзаголовки, описания).
  /// Светло-серый, чтобы не спорить с заголовками.
  static const Color textSecondary = Color(0xFFB3B3B3);

  /// Текст-заглушка или неактивные иконки.
  static const Color textDisabled = Color(0xFF666666);

  // ---------------------------------------------------------------------------
  // РАЗНОЕ
  // ---------------------------------------------------------------------------

  /// Тонкие разделители и границы (Borders).
  static const Color divider = Color(0xFF2F2F2F);
}